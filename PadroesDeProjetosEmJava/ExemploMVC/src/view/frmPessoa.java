    // Cria um método que preenche a tabela jTable
    void preencheTabela() {
        // Define as colunas para o jTable
        String colunas[] = new String[]{"Código", "Nome", "Idade"};
        // Instancia o modelo da tabela
        DefaultTableModel modeloTabela = new DefaultTableModel(colunas, 0);
        // Cria um objeto da classe PessoaController
        PessoaController pe = new PessoaController();
        // Cria uma lista que receberá os objetos do modelo 'Pessoa'
        ArrayList<Pessoa> listaPessoas = pe.buscaPessoas();
        // Percorre a lista de pessoas
        for (Pessoa pessoa : listaPessoas) {
            // Define as linhas para o jTable
            String linha[] = new String[]{String.valueOf(pessoa.getIdPessoa()),
                pessoa.getNome(), String.valueOf(pessoa.getIdade())};
            // Insere a linha no modelo da tabela
            modeloTabela.addRow(linha);
        }
        //Adicionar os dados no jTable
        tblPessoas.setModel(modeloTabela);
        // Define a largua deseja para as colunas
        tblPessoas.getColumnModel().getColumn(0).setPreferredWidth(50);
        tblPessoas.getColumnModel().getColumn(1).setPreferredWidth(200);
        tblPessoas.getColumnModel().getColumn(2).setPreferredWidth(50);
    }

