    private void btnSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSalvarMouseClicked
        String msg = "";
        int idade = Integer.parseInt(txtIdade.getText());
        // Instancia um objeto da classe PessoaController
        PessoaController pe = new PessoaController();
        if (idPessoa < 0) {
            // Chama o método da classe PessoaController
            msg = pe.cadastraPessoa(txtNome.getText(), idade);
        } else {
            msg = pe.atualizaPessoa(idPessoa,txtNome.getText(), idade);
        }

        // Exibe a mensagem para o usuário
        JOptionPane.showMessageDialog(null, msg);
        // Atualiza os dados da tabela
        this.preencheTabela();
        ativaBotoes(true, false, false, false);
        limpaCampos();

    }//GEN-LAST:event_btnSalvarMouseClicked

