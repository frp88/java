    private void tblPessoasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPessoasMouseClicked
        int indice = tblPessoas.getSelectedRow();
        if (indice >= 0) {
            int linhaSel = tblPessoas.getSelectedRow();
            //int colunaSel = tblPessoas.getSelectedColumn();  
            idPessoa = Integer.parseInt(tblPessoas.getValueAt(linhaSel, 0).toString());
            txtNome.setText(tblPessoas.getValueAt(linhaSel, 1).toString());
            txtIdade.setText(tblPessoas.getValueAt(linhaSel, 2).toString());
        }
        ativaBotoes(true, true, true, true);
    }//GEN-LAST:event_tblPessoasMouseClicked

